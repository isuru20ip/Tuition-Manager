    private void loadStudents(String query) {
        try {

            ResultSet resultSet = DB.search(query);

            DefaultTableModel tableModel = (DefaultTableModel) studentTable.getModel();
            tableModel.setRowCount(0);

            while (resultSet.next()) {
                Vector vector = new Vector();
                vector.add(resultSet.getString("id"));
                vector.add(resultSet.getString("fname"));
                vector.add(resultSet.getString("lname"));
                vector.add(resultSet.getString("birthday"));
                vector.add(resultSet.getString("nic"));
                vector.add(resultSet.getString("mobile"));
                vector.add(resultSet.getString("email"));

                tableModel.addRow(vector);
            }

        } catch (Exception e) {
            e.printStackTrace();
        }
    }

    private void search() {
        String studenId = studentIDField.getText();
        loadStudents("SELECT * FROM `student` WHERE `id` LIKE '%" + studenId + "%' ORDER BY `student`.`id` ASC");
        studentIDField.setText(studenId);
    }

    private void search1() {
        String nic = studentNicField.getText();
        loadStudents("SELECT * FROM `student` WHERE `nic` LIKE '%" + nic + "%' ORDER BY `student`.`id` ASC");
        studentNicField.setText(nic);
    }

